// switch statement

const grade = "C";

switch (grade) {
  case "F":
    console.log("Study harder!");
    break;
  case "E":
    console.log("You need to do better!");
    break;
  case "D":
    console.log("Mediocre");
    break;
  case "C":
    console.log("Good enough");
    break;
  case "B":
    console.log("Very good!");
    break;
  case "A":
    console.log("Excellent!");
    break;
  default:
    console.log("Drop out!");
}
